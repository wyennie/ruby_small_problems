=begin
PROBLEM
Given an unordered array that you know has exactly one value in the array that 
occurs twice, how do you determine which value occurs twice? Write a method
that will find and return the duplicate value in the array.

Explicit
-one array as an argument
  -array is unordered
  -only one value occurs more than once


EXAMPLES
