          40, 23, 71, 62, 73, 32, 43, 24,  4, 56,
          7,  34, 57, 74, 45, 11, 88, 67,  5, 58]) == 73

DATA STRUCTURES / ALGORITHM
ITERATE through the array
  IF the curent value equal ITERATE through the array
    then return that value

DEF find_dup five array as argument
  ITERATE through array
    IF current_value equals (ITERATE through each value of the array)
      RETURN value
