def find_dup(array)
  potential_dupes = []
  count = 0

  loop do
    if potential_dupes.include?(array[count])
      potential_dupes[count] = array[count]
      break
    else
      potential_dupes[count] = array[count]
      count += 1 
    end
  end
  
  potential_dupes[count]
end

